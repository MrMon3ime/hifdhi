export const generateWeeklyProgressData = (lang = 'ar') => {
  const days = lang === 'ar'
    ? ['الأحد', 'الاثنين', 'الثلاثاء', 'الأربعاء', 'الخميس', 'الجمعة', 'السبت']
    : ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];
  return days.map((day, i) => ({
    day,
    verses: [12, 18, 8, 22, 15, 0, 10][i],
    attendance: [8, 7, 8, 6, 8, 0, 5][i],
  }));
};

